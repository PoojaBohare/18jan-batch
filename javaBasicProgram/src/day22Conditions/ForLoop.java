package day22Conditions;

public class ForLoop {
	public static void main(String args[]) {
		
		for(int i=1;i<11;i++) {
			System.out.println(i);
		}
		System.out.println("*************Reverse****************");
		for(int i=10;i>0;i--) {
			System.out.println(i);
		}
		
		for(int i=1;i<=10;i++) {
			System.out.println(i + " : Hello....");
			
		}
		for(int i=10;i>0;i--) {
			System.out.println(i + " : Hello....");
			
			
		}
		for(int i=1;i<11;i++) {
			System.out.println(i);//for 1 to 10
			
		}
		for(int i=10;i>0;i--) {
			System.out.println(i);
			
		}
	}
}