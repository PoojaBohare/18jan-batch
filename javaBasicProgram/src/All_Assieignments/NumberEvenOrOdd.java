package All_Assieignments;

public class NumberEvenOrOdd {

	public static void main(String[] args) {
		for(int i =1;i<=100;i++) {
			if(i%2==0) {
				System.out.println("Number is Even "+i);
				
			}else {
				System.out.println("Number is Odd "+i);
				
			}
			
		}
		//for specific number
		int i=90;
		if(i%2==0) {
			System.out.println("Number is even "+i);
			
		}else {
			System.out.println("Number is not even "+i);
		}
		

	}

}
